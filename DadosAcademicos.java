public class DadosAcademicos {
    private String matricula;
    private String serie;
    private Escola escola;
    private Turno turno;
    
    public DadosAcademicos(String matricula, String serie, Escola escola, Turno turno) {
        this.matricula = matricula;
        this.serie = serie;
        this.escola = escola;
        this.turno = turno;
    }
 
}
